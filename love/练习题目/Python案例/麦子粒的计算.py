def ratio(n):
    a_1 = 1  # 第一项为1
    q = 2  # 公比为2
    if 1 <= n <= 64:  # 判断n的大小是否处于1到64之间
        a_n = a_1 * (q ** (n - 1))  # 当格数为n时,麦子粒为多少
        s_n = (a_1 * (1 - (q ** n))) / (1 - q)  # 求出总数
        print(f"当到第{n}格时")  # 打印第n格
        print(f"当格数为{n}时, 麦子数量为{a_n}粒")  # 打印第n格时,麦子粒为多少
        return f"到第{n}格时, 麦子粒总数为{s_n}"  # 打印到第n格时,麦子粒总数为多少
    else:
        return "输入的格数不规范,应在[1, 64]区间"


if __name__ == '__main__':
    x = int(input("当放到第:"))
    print(ratio(x))